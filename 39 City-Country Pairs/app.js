"use strict";
// Question 39: City Names: Formatting city-country pairs.
// Returning formatted strings from functions can simplify data 
// presentation. This exercise practices string
// formatting and returning values from functions.
function city_country(city, country) {
    return ` ${city} , ${country}`;
}
console.log(city_country("Islamabad", "Pakistan"));
console.log(city_country("Amercia", "Chicago"));
console.log(city_country("India", "Delhi"));
